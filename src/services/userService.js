const db = require("../models");
import bcrypt from "bcrypt";
const saltRounds = 10;

let hashUserPassword = (password) => {
  return new Promise(async (resolve, reject) => {
    try {
      let hashPassWord = await bcrypt.hashSync(password, saltRounds);
      resolve(hashPassWord);
    } catch (e) {
      reject(e);
    }
  });
};

